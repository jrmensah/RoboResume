package com.company;

public class College {
    public String name;
    public String major;
    public String degree;

    public String getName() {
        return name;
    }

    public void setName(String college) {
        this.name = college;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }
}
